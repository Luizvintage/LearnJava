public class variaveis {
    public static void main(String[] args){
        //Tipos Primitivos
        int idade = 25; //inteiro
        double altura = 1.79; //decimal
        char inicial = 'L'; //único Caractere
        boolean estudando = true; //Verdadeiro ou falso

        //String
        String nome = "Luiz";

        // Exibir as variáveis no console
        System.out.println("Nome: "+ nome);
        System.out.println("Idade: "+ idade);
        System.out.println("Altura: "+ altura);
        System.out.println("Inicial do nome: "+ inicial);
        System.out.println("Está estudando: "+ estudando);


    }
}
